package conditional_statements;

public class RRR_practiced
{

	public static void main(String[] args) 
	{
		int n=10;
		switch(n)
		{
		case 1:
			System.out.println("it is monday");break;
		case 2:
			System.out.println("it is tuesday");break;
		case 3:
			System.out.println("it is wednesday");break;
		case 4:
			System.out.println("it is thurseday");break;
		case 5:
			System.out.println("it is friday");break;
		case 6:
			System.out.println("it is satuerday");break;
		case 7:
			System.out.println("it is sunday");break;
		default:
			System.out.println("it is not day");break;
		}

	}

}
